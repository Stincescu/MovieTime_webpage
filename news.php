<h2 id = "article_3">'Puss in Boots 2' Gets an Official Title, Release Date and a New Director</h2><center>
<img class="news" src="images/stire3.jpg">
<p>
Universal has finally set a release date for the Puss in Boots sequel, which also now has an official title, according to a report by Deadline. Puss in Boots: The Last Wish 
will premiere September 23, 2022, and will bring back the smooth feline in a new adventure, following the commercial success of the 2011 Shrek spin-off.
</P>
<p>
The sequel will bring back Antonio Banderas to voice Puss in Boots as he journeys after the mythical Last Wish, the only thing that can restore the eight lives he already lost.
 It’s a simple premise that could potentially lead to a great film, but all the changes during the movie’s production have led to a long wait for fans who have been eagerly anticipating a sequel.
</P>
<p>
The first Puss in Boots was a commercial success, with an impressive international box office of $554 million, making a sequel almost mandatory for DreamWorks. However, it’s been 
10 years already, and in the meantime, a sequel movie for Puss in Boots was announced and shelved, and rumors of reboots both for Puss in Boots and Shrek began to swirl. The last
 time we heard about Puss in Boots: The Last Wish, still without an official name, a different director was attached to the project.
</P>
<p>
Now, instead of Bob Persichetti (Spider-Man: Into the Spider-Verse), Puss In Boots: The Last Wish will be helmed by Joel Crawford and produced by Mark Swift. Crawford and Swift did 
an amazing job working together in The Croods: A New Age, which earned them a Golden Globe nomination for Best Picture.
</P>
<p>
Since we have an official name, the next step is for DreamWorks to release a trailer for Puss in Boots: The Last Wish. As soon as it happens, we’ll be able to take a look at what came
 out of this long and winding development road and be sure to report back here.
</P>
<p>
Puss in Boots: The Last Wish is set to open September 23, 2022.
</p>

