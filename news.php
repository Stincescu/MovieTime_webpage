	<h2 id = "article_2">China Box Office: ‘Avatar’ Leads With a Further $14 Million</h2>
	<center><img class ="news" src="images/stire2.jpg">
<p>
James Cameron’s “Avatar” once again led the China box office this weekend, with sales of $14 million over the three-day period.
</P>
<p>
The 2009 film has held its own as the top title every day since its March 12 re-release in China. It has grossed a total of $44 million so far, ten days into its re-run.
When it screened in China in 2010, it was a massive hit, raking in $203 million.
</P>
<p>
Thanks to recent China sales, the sci-fi epic has now regained its crown from “Avengers: Endgame” as the highest grossing movie in history. “Avatar” had held that title 
for a decade with $2.7897 billion in global sales. But in April 2019, “Avengers” squeaked past it with a $2.7902 billion cume. The recent re-release now puts “Avatar” 
squarely back in position as the title to beat once again.
</P>
<p>
“We are proud to reach this great milestone, but Jim and I are most thrilled that the film is back in theaters during these unprecedented times, and we want to thank our
Chinese fans for their support,” “Avatar” producer Jon Landau said last week as the film pushed past the Marvel franchise.
</p>
