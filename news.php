	<h2 id = "article_1"><a id ="article_1">The Oscars Just Announced Its Hosting Plans For This Year</a></h2>
	
	<center><img class="news" src="images/stire1.jpg">
	<p>
	In a year when awards shows are trying to adapt to the climate that pandemic living has imposed upon us all, the producers behind this year’s 93rd Academy Awards
	seem to be trying to maintain their usual routine. With attendees not being offered an option to Zoom into the show, or wear hoodies while attending, the Oscars 
	ceremony is trying to keep the traditional glitz it's always possessed. And yet, the hosting plans for this year’s show have been announced, and I’m not here for
	the continued lack of them.
	</p>
	<p>
	That’s right, the Academy of Motion Pictures Arts and Sciences are, yet again, going without a host. Deadline caught wind of these plans through sources close to
	the planning phases of this year’s Oscars telecast. Instead, offers have been put out to presenters that will introduce the various awards, special honors, and 
	segments for the evening, just like they have for the past two years. Which is probably the biggest mistake this year’s show could make, as far as I’m concerned.
	</p>
	<p>
	Yes, hosts can be awkward, especially when you’re holding an awards ceremony during the still raging pandemic that’s ever present. But in a year when Oscar producers
	 Jesse Collins, Stacey Sher and Steven Soderbergh are urging attendees to “read the room” when on the stage at this year's ceremony, it’s kind of surprising that the
	  first part of that big equation isn’t being heeded. If the Oscars are trying to play to a sense of normalcy and pageantry, it’s time to bring back the hosting gig.
	</p>

